package com.syntax.class07;

public class forLoop {

	public static void main(String[] args) {
		// лучший вариант если ты знаешь количество вхождений в цикл
		for (int b=1;b<=5;b++) {
			System.out.println(b+". Good morning");
		}
		for (int a=0;a<=50;a+=5) {
			System.out.print(a+" ");
		}

	}

}
